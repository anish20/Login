    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        try {
            User u=new User();
            u.setUserid(Integer.parseInt(userid.getText()));
            u.setName(name.getText());
            u.setAddress(address.getText());
            u.setPass(password.getText());
            if(male.isSelected()){
                u.setGender("Male");
            }if(female.isSelected()){
                u.setGender("Female");
            }
            u.setAge(Integer.parseInt(age.getText()));
            u.setMobile_no(mobile.getText());
            Connection con=DBConnectivity.ConnectDb.getConnect();
            Statement st=con.createStatement();
            String Query="insert into Usertbl values('"+u.getUserid()+"','"+u.getName()+"','"+u.getAddress()+"','"+u.getPass()+"','"+u.getGender()+"','"+u.getAge()+"','"+u.getMobile_no()+"')";
            int i=st.executeUpdate(Query);
            if(i>0){
                JOptionPane.showMessageDialog(this, "Data Inserted..");
                new LoginForm().setVisible(true);
                this.hide();
            }else{
                JOptionPane.showMessageDialog(this, "Data not Inserted..");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_registerButtonActionPerformed

