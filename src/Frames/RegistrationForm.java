    private void mobileKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mobileKeyTyped
       char c=evt.getKeyChar();
       if(!(Character.isDigit(c))){
           evt.consume();
       }
    }//GEN-LAST:event_mobileKeyTyped

